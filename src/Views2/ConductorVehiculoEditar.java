    private void nuevo_capacidad_button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nuevo_capacidad_button1ActionPerformed
        String capacidadStr = nuevo_capacidad_input1.getText();

    if (capacidadStr.isEmpty()) {
        nuevo_capacidad_message.setText("Ingrese una capacidad v√°lida");
        return;
    }

    try {
        int nuevaCapacidad = Integer.parseInt(capacidadStr);
        // Actualizar capacidad del veh√≠culo
        conductor.set_capacidad_vehiculo(nuevaCapacidad);
        boolean exito = conductor.actualizar_vehiculo(conductor.get_placa_vehiculo(), conductor.get_modelo_vehiculo(), nuevaCapacidad, st);
        
        if (!exito) {
            JOptionPane.showMessageDialog(this, "No se pudo actualizar la capacidad del veh√≠culo", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        JOptionPane.showMessageDialog(this, "Capacidad del veh√≠culo actualizada exitosamente!!", "√âxito", JOptionPane.INFORMATION_MESSAGE);
        nuevo_capacidad_input1.setText(""); // Limpiar el campo de entrada
    } catch (NumberFormatException e) {
        nuevo_capacidad_message.setText("Ingrese un n√∫mero v√°lido para la capacidad");
    }


    }//GEN-LAST:event_nuevo_capacidad_button1ActionPerformed

